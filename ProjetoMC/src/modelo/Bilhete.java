package modelo;

public class Bilhete {
	private int id_bilhete;
	private String nome_bilhete;
	private float vlr_apostado;
	
	
	public int getId_bilhete() {
		return id_bilhete;
	}
	public void setId_bilhete(int id_bilhete) {
		this.id_bilhete = id_bilhete;
	}
	public String getNome_bilhete() {
		return nome_bilhete;
	}
	public void setNome_bilhete(String nome_bilhete) {
		this.nome_bilhete = nome_bilhete;
	}
	public float getVlr_apostado() {
		return vlr_apostado;
	}
	public void setVlr_apostado(float vlr_apostado) {
		this.vlr_apostado = vlr_apostado;
	}
	
	
	
	
}
