package modelo;

public class Jogo {
	private int id_jogo;
	private String time_casa;
	private String time_fora;
	private String palpite;
	private String dt_jogo;
	private float cotacao;
	
	
	public int getId_jogo() {
		return id_jogo;
	}
	public void setId_jogo(int id_jogo) {
		this.id_jogo = id_jogo;
	}
	public String getTime_casa() {
		return time_casa;
	}
	public void setTime_casa(String time_casa) {
		this.time_casa = time_casa;
	}
	public String getTime_fora() {
		return time_fora;
	}
	public void setTime_fora(String time_fora) {
		this.time_fora = time_fora;
	}
	public String getPalpite() {
		return palpite;
	}
	public void setPalpite(String palpite) {
		this.palpite = palpite;
	}
	public String getDt_jogo() {
		return dt_jogo;
	}
	public void setDt_jogo(String dt_jogo) {
		this.dt_jogo = dt_jogo;
	}
	public float getCotacao() {
		return cotacao;
	}
	public void setCotacao(float cotacao) {
		this.cotacao = cotacao;
	}
	
	
	
	
	
	
	

}
